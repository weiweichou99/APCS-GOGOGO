/******************************************************************************

在 BASIC 語言中有一個 SGN 函數，(VB.Net 改成 Math.Sign)，
它會根據參數的正負號傳回 1, 0, 或 -1。
請你寫一個程式來模仿這個函數，給你一個整數 n，若 n > 0 請輸出 1，若 n = 0 請輸出 0，若 n < 0 請輸出 -1。

提示 ：
你能只用關係運算子及算術運算子來求出解答，而不需要用到 if 指令嗎？

標籤:
關係運算子

主要修改內容：

新增輸入整數 n 的程式碼
使用關係運算子來實現 SGN 函數的功能，不使用 if 判斷式
運算式 (n > 0) - (n < 0) 的運作原理：
當 n > 0 時：(1) - (0) = 1
當 n = 0 時：(0) - (0) = 0
當 n < 0 時：(0) - (1) = -1
這樣就能完成題目要求，不使用 if 指令也能實現 SGN 函數的功能。

*******************************************************************************/
#include <bits/stdc++.h>
using namespace std;

// 宣告 SGN 函數
int SGN(int n)
{
    return (n > 0) - (n < 0);
}

int main()
{
    int n;
    cin >> n;
    cout << SGN(n);
    return 0;
}